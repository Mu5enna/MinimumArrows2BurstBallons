public class Pair{
    private int first, second;

    public Pair() { this.first=0; this.second=0; }

    public int getFirst(){ return first; }
    public int getSecond(){ return second; }
    public void setFirst(int first) {
        this.first = first;
    }
    public void setSecond(int second) {
        this.second = second;
    }
}
